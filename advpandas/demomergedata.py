import pandas as pd
customers=pd.read_excel("C://mydataset//hclorders.xlsx",sheet_name='customers')
orders=pd.read_excel("C://mydataset//hclorders.xlsx",parse_dates=["order_date"],sheet_name="orders")
products=pd.read_excel("C://mydataset//hclorders.xlsx",sheet_name="products")
result=pd.merge(left=customers,right=orders,on="cust_id",how="inner")
result1=pd.merge(left=customers,right=orders,on="cust_id",how="outer")
result2=pd.merge(left=customers,right=orders,on="cust_id",how="left",indicator="both")
result3=pd.merge(left=customers,right=orders,on="cust_id",how="right")
res=customers.merge(orders,on="cust_id").merge(products,on="order_id")
con=pd.concat([customers,orders],axis=1)
